/*- Assignment - 05
    Write a program to print the difference and product of the numbers 45 and 32.
*/
package Lets_program;

public class Assignment_05 {
    public static void main(String[] args) {
        int a = 45, b = 32;
        int difference = a - b;
        int product = a * b;
        System.out.println("Difference of the Numbers : " + difference);
        System.out.println("Product of the Numbers : "+product);
    }
}
